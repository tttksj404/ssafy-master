# find(x): 노드 x가 속한 집합의 '대장(루트)'을 찾는 함수입니다.
def find(x):
    # 만약 내 부모가 나 자신이 아니라면, 아직 진짜 대장을 찾지 못한 것입니다.
    if parent[x] != x:
        # 내 부모를 '부모의 대장'으로 바꿉니다. (이 과정을 '경로 압축'이라고 하며, 속도를 엄청나게 빨라지게 합니다.)
        parent[x] = find(parent[x])
    return parent[x]

# union(a, b): 두 노드 a와 b를 하나의 팀으로 합치는 함수입니다.
def union(a, b):
    rootA = find(a) # a의 대장을 찾고
    rootB = find(b) # b의 대장을 찾습니다.
    
    # 두 대장이 다르다면(서로 다른 팀이라면) 합쳐줍니다.
    if rootA != rootB:
        # 번호가 더 작은 쪽을 대장으로 삼기로 약속합니다. (일관성 있는 트리 구조를 위해)
        if rootA < rootB:
            parent[rootB] = rootA
        else:
            parent[rootA] = rootB
        



