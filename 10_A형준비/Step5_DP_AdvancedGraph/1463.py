"""
📍 [BOJ] 1463 1로 만들기
> [학습 우선순위: Step 5 - DP/기초]
> 3으로 나누기, 2로 나누기, 1 빼기 세 연산을 최소로 써서 1을 만드는 방법.

🔍 상세 분석:
- 다이내믹 프로그래밍(DP)의 입문 문제입니다.
- **"어떤 숫자 i를 1로 만드는 최소 횟수"**는 그보다 작은 숫자들의 정답으로부터 구할 수 있습니다.
- 점화식: `dp[i] = min(dp[i-1], dp[i/2], dp[i/3]) + 1`

🏗️ 구현 체크리스트:
1. `dp` 테이블을 0부터 N까지 준비한다.
2. 2부터 N까지 차례대로 올라가며(Bottom-up) 값을 채운다.
3. 일단 `dp[i-1] + 1`을 기본으로 잡고, 2나 3으로 나누어 떨어지면 그 경우와 비교해 최솟값을 택한다.

💡 학생 가이드:
- "그리디(큰 수로 나누기)가 왜 안 되나요?" 
  -> 10의 경우: 10/2=5, 5-1=4, 4/2=2, 2/2=1 (4번) 이지만 10-1=9, 9/3=3, 3/3=1 (3번) 이 더 빠릅니다. 그래서 모든 경우를 다 기록하는 DP가 필요합니다!
"""

import sys
input = sys.stdin.readline

def solve():
    N = int(input())
    
    # dp[i]는 숫자 i를 1로 만드는 데 필요한 최소 연산 횟수
    dp = [0] * (N + 1)
    
    # 2부터 시작해서 N까지 정답을 쌓아 올린다
    for i in range(2, N + 1):
        # 1. 먼저 1을 빼는 경우를 가정한다 (i-1에서 오는 것)
        dp[i] = dp[i - 1] + 1
        
        # 2. 만약 2로 나누어 떨어진다면? (i/2에서 오는 것과 비교)
        if i % 2 == 0:
            dp[i] = min(dp[i], dp[i // 2] + 1)
            
        # 3. 만약 3으로 나누어 떨어진다면? (i/3에서 오는 것과 비교)
        if i % 3 == 0:
            dp[i] = min(dp[i], dp[i // 3] + 1)
            
    print(dp[N])

if __name__ == "__main__":
    solve()
