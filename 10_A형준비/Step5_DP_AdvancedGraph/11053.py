"""
📍 [BOJ] 11053 가장 긴 증가하는 부분 수열 (LIS)
> [학습 우선순위: Step 5 - DP/LIS]
> 수열에서 가장 긴 증가하는 부분 수열의 길이를 구하라.

🔍 상세 분석:
- **LIS**의 가장 기본형입니다.
- i번째 숫자를 마지막으로 하는 수열의 길이는, i보다 앞선 j들 중 `nums[j] < nums[i]` 인 것들 중 가장 큰 `dp[j]` 에 1을 더한 것입니다.
- 시간 복잡도: O(N^2). N=1,000 이하일 때 적합합니다.

🏗️ 구현 체크리스트:
1. `dp` 리스트를 모두 1로 초기화한다. (모든 숫자는 자기 자신만으로 길이 1인 수열임)
2. 이중 for문으로 현재 숫자와 이전 숫자들을 전부 비교한다.
3. `if nums[j] < nums[i]: dp[i] = max(dp[i], dp[j] + 1)`

💡 학생 가이드:
- "왜 1로 시작하나요?" 
  -> 어떤 수열이든 숫자 하나만 있어도 길이는 1이기 때문입니다. 앞의 어떤 숫자와도 연결이 안 될 때의 기본값인 셈이죠.
"""

import sys
input = sys.stdin.readline

def solve():
    N = int(input())
    nums = list(map(int, input().split()))
    
    # dp[i]는 nums[i]를 마지막으로 포함하는 LIS의 길이
    dp = [1] * N
    
    # 1. 모든 숫자를 하나씩 기준으로 삼아 확인
    for i in range(N):
        # 2. 기준 숫자보다 앞에 있는 숫자들을 전부 조사
        for j in range(i):
            # 앞의 숫자가 나보다 작다면? (증가하는 관계!)
            if nums[j] < nums[i]:
                # 그 숫자의 최적해 뒤에 내가 붙는 것과 현재 나의 값 중 큰 쪽 선택
                dp[i] = max(dp[i], dp[j] + 1)
                
    # 3. 전체 기록 중 가장 큰 값이 정답
    print(max(dp))

if __name__ == "__main__":
    solve()
