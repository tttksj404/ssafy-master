"""
📍 [BOJ] 1912 연속합
> [학습 우선순위: Step 5 - DP/수열]
> 수열에서 연속된 몇 개의 수를 선택해 구할 수 있는 가장 큰 합을 구하라.

🔍 상세 분석:
- **카데인 알고리즘(Kadane's Algorithm)**으로 알려진 기법입니다.
- i번째 숫자까지 왔을 때 고민은 하나뿐입니다:
  - "이전까지의 합에 나를 붙일까? 아니면 그냥 나부터 새로 시작할까?"
- 이전 합이 음수라면 그냥 나부터 새로 시작하는 게 무조건 이득입니다!

🏗️ 구현 체크리스트:
1. `dp[i]` 를 i번째 숫자를 마지막으로 포함하는 최대 연속합으로 정의한다.
2. `dp[0]`은 수열의 첫 번째 수로 시작한다.
3. 반복문을 돌며 `dp[i] = max(nums[i], dp[i-1] + nums[i])` 를 수행한다.

💡 학생 가이드:
- "따로 max_val을 관리해야 하나요?" 
  -> 네, `dp` 배열에는 각 위치에서의 최선의 결과가 담겨 있으므로, 그중 가장 큰 값을 정답으로 출력하면 됩니다.
"""

import sys
input = sys.stdin.readline

def solve():
    n = int(input())
    nums = list(map(int, input().split()))
    
    # dp[i]는 i번째 숫자를 끝으로 하는 연속합의 최대치
    dp = [0] * n
    dp[0] = nums[0]
    
    # 1. 수열을 돌며 최선의 선택을 기록
    for i in range(1, n):
        # [나부터 새로 시작] vs [이전 합에 나를 포함]
        # 만약 dp[i-1]이 음수라면 nums[i]가 더 커지게 됨
        dp[i] = max(nums[i], dp[i - 1] + nums[i])
        
    # 2. 기록된 값들 중 가장 큰 것이 전체 수열의 최대 연속합
    print(max(dp))

if __name__ == "__main__":
    solve()
