"""
📍 [BOJ] 11727 2×n 타일링 2
> [학습 우선순위: Step 5 - DP/타일링]
> 2×n 직사각형을 1×2, 2×1, 2×2 타일로 채우는 방법의 수.

🔍 상세 분석:
- n번째 칸을 채우는 마지막 모양은 세 가지입니다:
  1. 2×1 타일 1개로 끝남 (남은 공간 n-1)
  2. 1×2 타일 2개로 끝남 (남은 공간 n-2)
  3. 2×2 타일 1개로 끝남 (남은 공간 n-2)
- 점화식: `dp[n] = dp[n-1] + 2 * dp[n-2]`

🏗️ 구현 체크리스트:
1. `dp[1] = 1`, `dp[2] = 3` 초기화.
2. 10,007로 나누는 것을 잊지 말 것.
3. n=1인 경우 인덱스 에러 방지.

💡 학생 가이드:
- "왜 1x2 타일 두 개와 2x2 타일 하나를 따로 보나요?" 
  -> 모양이 다르기 때문입니다! n-2 공간을 채우고 나서 뒤에 붙일 수 있는 '서로 다른 끝 모양'이 2종류라는 뜻이죠.
"""

import sys
input = sys.stdin.readline

def solve():
    n = int(input())
    # 1. 예외 처리 (n이 작을 때)
    if n == 1:
        print(1); return
    
    dp = [0] * (n + 1)
    
    # 2. 초기값 설정
    dp[1] = 1
    dp[2] = 3 # (세로2), (가로2), (정사각1)
    
    # 3. 점화식 적용
    for i in range(3, n + 1):
        # i-1에서 오는 경우 1가지 + i-2에서 오는 경우 2가지(가로2 or 정사각1)
        dp[i] = (dp[i - 1] + 2 * dp[i - 2]) % 10007
        
    print(dp[n])

if __name__ == "__main__":
    solve()
