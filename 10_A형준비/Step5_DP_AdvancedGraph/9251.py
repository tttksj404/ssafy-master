"""
📍 [BOJ] 9251 LCS (최장 공통 부분 수열)
> [학습 우선순위: Step 5 - DP/문자열]
> 두 문자열이 주어졌을 때, 모두의 부분 수열이 되는 수열 중 가장 긴 것의 길이를 구하라.

🔍 상세 분석:
- 두 문자가 같을 때와 다를 때의 점화식을 세우는 게 핵심입니다.
- **문자가 같으면**: 대각선 왼쪽 위(두 문자 다 없었을 때)의 길이에 1을 더합니다.
- **문자가 다르면**: 왼쪽이나 위쪽(둘 중 하나를 포기했을 때) 중 더 긴 길이를 가져옵니다.

🏗️ 구현 체크리스트:
1. `(길이1+1) x (길이2+1)` 크기의 2차원 DP 테이블을 만든다. (0번 행/열은 빈 문자열용)
2. 이중 for문으로 각 문자를 비교한다.
3. 같으면 `dp[i][j] = dp[i-1][j-1] + 1`
4. 다르면 `dp[i][j] = max(dp[i-1][j], dp[i][j-1])`

💡 학생 가이드:
- "왜 부분 문자열(Substring)과 다른가요?" 
  -> 부분 수열은 문자가 띄엄띄엄 있어도 순서만 맞으면 되기 때문입니다. 그래서 문자가 다를 때도 이전까지의 최적해를 계속 끌고 오는 것입니다.
"""

import sys
input = sys.stdin.readline

def solve():
    # 두 문자열 입력 (공백 제거)
    S1 = input().strip()
    S2 = input().strip()
    
    n, m = len(S1), len(S2)
    # 1. DP 테이블 초기화 (0행, 0열은 여유분)
    dp = [[0] * (m + 1) for _ in range(n + 1)]
    
    # 2. 모든 문자 쌍을 비교하며 테이블 채우기
    for i in range(1, n + 1):
        for j in range(1, m + 1):
            # i-1, j-1 인덱스를 쓰는 이유는 DP 테이블과 문자열 인덱스 차이 때문!
            if S1[i - 1] == S2[j - 1]:
                # 문자가 같다면? 이전까지의 최장 길이에 1 더하기
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                # 문자가 다르다면? 한 글자씩 뺀 상태 중 더 긴 것 가져오기
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
                
    # 최종 우측 하단 값이 최장 공통 부분 수열의 길이
    print(dp[n][m])

if __name__ == "__main__":
    solve()
