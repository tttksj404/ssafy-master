"""
📍 [BOJ] 12865 평범한 배낭
> [학습 우선순위: Step 5 - DP/배낭]
> 최대 무게 K인 배낭에 담을 수 있는 물건들의 가치 합의 최댓값을 구하라.

🔍 상세 분석:
- 유명한 **냅색(Knapsack) 알고리즘**입니다.
- 물건을 쪼갤 수 없으므로(0/1 Knapsack), 각 무게별로 가질 수 있는 최대 가치를 계속 갱신합니다.
- 점화식: `dp[w] = max(dp[w], dp[w - 물건무게] + 물건가치)`

🏗️ 구현 체크리스트:
1. `dp` 배열을 0부터 K까지 만든다.
2. 각 물건에 대해 반복문을 돌린다.
3. 중요: **무게 K부터 거꾸로 역순으로** 돌면서 `dp`를 갱신한다! (그래야 같은 물건이 중복해서 쓰이지 않음)

💡 학생 가이드:
- "왜 거꾸로 도나요?" 
  -> 앞에서부터 돌면, 내가 방금 넣은 물건을 다음 무게에서 또 넣는 꼴이 되어버립니다(무한 배낭). 우리는 물건을 딱 1개씩만 가지고 있으므로 뒤에서부터 채워야 '이전 물건'의 데이터만 참고하게 됩니다.
"""

import sys
input = sys.stdin.readline

def solve():
    # N: 물건 개수, K: 배낭 한도 무게
    N, K = map(int, input().split())
    # dp[w]는 무게 w를 채웠을 때 얻을 수 있는 최대 가치
    dp = [0] * (K + 1)
    
    for _ in range(N):
        w, v = map(int, input().split()) # 현재 물건의 무게와 가치
        
        # 1. 배낭 한도(K)부터 현재 물건 무게(w)까지 역순으로 훑는다
        for j in range(K, w - 1, -1):
            # [기존의 j무게 가치] vs [현재 물건을 넣기 전(j-w)의 가치 + 현재 가치]
            # 더 큰 쪽으로 갱신!
            dp[j] = max(dp[j], dp[j - w] + v)
            
    # 최종적으로 한도 K일 때의 최대 가치 출력
    print(dp[K])

if __name__ == "__main__":
    solve()
