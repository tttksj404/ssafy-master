"""
📍 [개념] 피보나치 수열 (Fibonacci Sequence)
> [학습 우선순위: Step 5 - DP/기초]
> 피보나치 수열은 F(n) = F(n-1) + F(n-2)로 정의됩니다. 이 문제를 가장 효율적으로 푸는 방법을 마스터합니다.

🔍 상세 분석:
- **문제점**: 단순 재귀(`fibo(n-1) + fibo(n-2)`)는 같은 계산을 수없이 반복하여 시간 복잡도가 O(2^n)에 달합니다. `fibo(5)`만 해도 `fibo(3)`이 두 번 호출되는 식입니다.
- **해결책**: 한 번 계산한 값을 저장해두고 재활용하는 **메모이제이션(Memoization)** 기법을 사용합니다. 이것이 동적 계획법(DP)의 핵심입니다.

🏗️ 구현 체크리스트:
1. 계산된 값을 저장할 `memo` 리스트(또는 딕셔너리)를 준비합니다.
2. 재귀 함수 시작 시, 이미 `memo`에 값이 있는지 먼저 확인합니다.
3. 값이 없다면 계산을 수행하고, 그 결과를 `memo`에 기록한 뒤 반환합니다.

💡 학생 가이드:
- "DP 테이블은 왜 필요한가요?" 
  -> 똑같은 계산을 두 번 다시 하지 않기 위해서입니다. `memo`라는 '족보'를 만들어서, 한 번 푼 문제는 다시 풀지 않고 정답만 보고 넘어가는 원리입니다.
- 이 메모이제이션 기법은 모든 DP 문제의 기초가 되므로 반드시 암기해야 합니다.
"""

# DP(메모이제이션)를 사용한 최적화된 피보나치 함수
memo = {0: 0, 1: 1} # 기저 사례를 미리 저장

def fibo_dp(n):
    """
    피보나치 수열의 n번째 항을 DP(메모이제이션) 방식으로 구합니다.
    
    Args:
        n (int): 구하고자 하는 항의 인덱스.
    
    Returns:
        int: n번째 피보나치 수.
    """
    # 1. 이미 계산한 적이 있는지 '족보(memo)'를 먼저 확인
    if n in memo:
        return memo[n]
    
    # 2. 계산한 적이 없다면, 점화식에 따라 계산
    # 이 과정에서 fibo_dp(n-1)과 fibo_dp(n-2)가 호출되며,
    # 그 결과들도 각각 memo에 저장됩니다.
    result = fibo_dp(n - 1) + fibo_dp(n - 2)
    
    # 3. 다음을 위해 계산된 결과를 '족보'에 기록
    memo[n] = result
    
    return result

# --- 사용 예시 ---
target_n = 10
print(f"{target_n}번째 피보나치 수는 {fibo_dp(target_n)}입니다.") # 출력: 10번째 피보나치 수는 55입니다.

target_n = 30
print(f"{target_n}번째 피보나치 수는 {fibo_dp(target_n)}입니다.") # 출력: 30번째 피보나치 수는 832040입니다.
# 단순 재귀였다면 이 계산은 매우 오래 걸렸을 것입니다.
