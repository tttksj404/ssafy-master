"""
📍 [BOJ] 1477 휴게소 세우기
> [학습 우선순위: Step 4 - 이진탐색/파라메트릭서치]
> 휴게소가 없는 구간의 길이의 최댓값을 최소로 하기 위해 휴게소 M개를 새로 세울 때, 그 최댓값을 구하라.

🔍 상세 분석:
- **"휴게소 간격이 X일 때, 추가로 세워야 하는 휴게소의 개수가 M개 이하인가?"**
- 간격이 좁을수록 더 많은 휴게소를 세워야 합니다.
- 도로의 시작(0)과 끝(L)을 포함하여 정렬한 뒤, 각 사이 거리마다 몇 개를 더 지을 수 있는지 계산합니다.

🏗️ 구현 체크리스트:
1. 기존 휴게소 위치에 `0`과 `L`을 추가하고 정렬한다.
2. `start = 1`, `end = L-1` 설정.
3. `mid` 간격으로 지었을 때 필요한 휴게소 개수 합산.
   - `(두 휴게소 사이 거리 - 1) // mid` 가 그 사이에 추가 설치할 수 있는 개수입니다.
4. 개수가 M보다 많으면 간격을 넓혀야 하고, M 이하면 간격을 더 좁혀봅니다.

💡 학생 가이드:
- "왜 (dist-1) // mid 인가요?" 
  -> 거리가 20이고 간격이 10일 때, 정확히 10 위치에 하나만 지어야 합니다. 20 위치는 이미 휴게소가 있거나 도로 끝이니까요. 그래서 끝점 중복 방지를 위해 -1을 해주는 테크닉입니다.
"""

import sys
input = sys.stdin.readline

def solve():
    N, M, L = map(int, input().split())
    # 기존 휴게소 + 시작점(0) + 끝점(L) 모두 포함해서 정렬!
    rests = sorted([0] + list(map(int, input().split())) + [L])
    
    # 1. 탐색 범위 (휴게소 간의 거리 기준)
    start = 1
    end = L - 1
    
    ans = L
    while start <= end:
        # mid를 '휴게소 간의 최대 간격'으로 가정!
        mid = (start + end) // 2
        
        # 2. 모든 구간을 mid 이하로 만들기 위해 필요한 추가 휴게소 개수 계산
        build_cnt = 0
        for i in range(1, len(rests)):
            dist = rests[i] - rests[i-1]
            if dist > mid:
                # 구간 내에 지을 수 있는 개수 합산
                # -1을 하는 이유는 끝점 위치(기존 휴게소 자리)에 짓는 걸 피하기 위함
                build_cnt += (dist - 1) // mid
                
        # 3. 추가 설치 개수가 M보다 많으면 간격이 너무 좁음
        if build_cnt > M:
            start = mid + 1
        # M개 이하면 간격을 더 좁혀서 '최소'를 찾으러 감
        else:
            ans = mid
            end = mid - 1
            
    print(ans)

if __name__ == "__main__":
    solve()
