"""
📍 [BOJ] 10974 모든 순열
> [학습 우선순위: Step 2 - 백트래킹/순열]
> 1부터 N까지의 수로 이루어진 모든 순열을 사전 순으로 출력하는 프로그램.

🔍 상세 분석:
- 순열(Permutation)은 '순서가 중요한' 나열입니다. (1, 2)와 (2, 1)은 다릅니다.
- DFS(깊이 우선 탐색)를 이용해 첫 번째 자리부터 숫자를 하나씩 채워나갑니다.
- 이미 사용한 숫자는 다시 쓰면 안 되므로 `visited` 배열이 꼭 필요합니다.

🏗️ 구현 체크리스트:
1. 숫자의 사용 여부를 기록할 `visited` 리스트를 만든다.
2. DFS 함수를 만들고, 현재 몇 개의 숫자를 뽑았는지(`depth`)와 뽑은 숫자 목록(`path`)을 관리한다.
3. 숫자를 하나 뽑을 때마다 `visited[i] = True`로 표시하고 재귀 호출한다.
4. 재귀가 끝나고 돌아오면 다시 `visited[i] = False`로 풀어서 다음 경우의 수에서 쓸 수 있게 한다. (백트래킹)

💡 학생 가이드:
- "원상 복구가 왜 중요한가요?" 
  -> (1, 2)를 다 만들고 나서 2번 자리에 3을 넣어 (1, 3)을 만들어야 합니다. 이때 2번 자리에 있었던 '2'를 다시 '사용 안 함' 상태로 돌려놓아야 나중에 (2, 1) 같은 걸 만들 때 2를 쓸 수 있기 때문입니다.
"""

import sys
input = sys.stdin.readline

def solve():
    # 1. N 입력 (예: N=3이면 1, 2, 3을 나열)
    N = int(input())
    # 2. 숫자를 썼는지 체크할 장부 (1번~N번까지 사용)
    visited = [False] * (N + 1)
    
    def dfs(depth, path):
        # [기저 조건] 숫자를 N개 다 뽑았을 때
        if depth == N:
            # 뽑은 순서대로 출력 (공백 구분)
            print(*path)
            return
            
        # [유도 파트] 1부터 N까지 숫자 중 아직 안 쓴 숫자를 찾는다
        for i in range(1, N + 1):
            if not visited[i]:
                # 1. 숫자 i를 쓴다고 표시하고 바구니에 넣는다
                visited[i] = True
                path.append(i)
                
                # 2. 다음 숫자를 채우러 더 깊이 들어간다
                dfs(depth + 1, path)
                
                # 3. 중요!! 탐색이 끝났으니 숫자 i를 다시 바구니에서 빼고 장부도 초기화 (백트래킹)
                path.pop()
                visited[i] = False
                
    # 0개 뽑은 상태로 탐색 시작
    dfs(0, [])

if __name__ == "__main__":
    solve()
