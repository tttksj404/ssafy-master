"""
📍 [BOJ] 1759 암호 만들기
> [학습 우선순위: Step 2 - 백트래킹/조합]
> 서로 다른 L개의 알파벳 소문자들로 구성되며 최소 한 개의 모음(a, e, i, o, u)과 최소 두 개의 자음으로 구성되어야 한다. 

🔍 상세 분석:
- 이 문제는 '주어진 문자들 중에서 순서를 고려하지 않고 L개를 뽑는' 조합(Combination) 문제입니다.
- 단, 뽑은 결과가 '알할벳 순(오름차순)'이어야 하므로, 처음에 문자들을 정렬해두면 편합니다.
- 시간 복잡도: C개 중 L개를 뽑는 조합 (C_C_L)인데, C와 L이 최대 15이므로 매우 작습니다.

🏗️ 구현 체크리스트:
1. 입력받은 문자들을 `chars.sort()`로 미리 정렬한다.
2. DFS를 이용해 문자를 하나씩 선택해 나간다.
3. 문자를 선택할 때, '현재 선택한 문자보다 뒤에 있는 문자'만 고르도록 인덱스(idx)를 넘겨준다.
4. L개를 다 골랐을 때, 모음 1개/자음 2개 조건을 만족하는지 검사한다.

💡 학생 가이드:
- "재귀 함수가 끝날 때 왜 `path.pop()`을 하나요?" 
  -> 내가 'a'를 넣고 탐색을 다 끝냈으니, 이제 'a'를 빼야 다음 루프에서 'b'를 넣고 탐색할 수 있기 때문입니다. (백트래킹의 핵심!)
"""

import sys
input = sys.stdin.readline

# L: 암호의 길이, C: 주어진 문자의 총 개수
L, C = map(int, input().split())
# 문자들을 리스트로 받아 오름차순 정렬 (미리 정렬하면 DFS 시 순서 고민이 사라짐)
chars = sorted(input().split())

# 모음 집합 (빠른 조회를 위해 set 사용)
vowels = {'a', 'e', 'i', 'o', 'u'}

def dfs(idx, path):
    """
    idx: 이번 단계에서 검사할 시작 문자의 위치 (정렬되어 있으므로 이 이전은 안 봄)
    path: 지금까지 내가 고른 문자들을 담은 바구니
    """
    
    # [기저 조건] 바구니에 문자가 L개 다 찼을 때!
    if len(path) == L:
        v_cnt = 0 # 모음 개수 카운트
        c_cnt = 0 # 자음 개수 카운트
        
        # 내가 고른 문자들을 하나씩 확인하며 모음/자음 개수 세기
        for char in path:
            if char in vowels:
                v_cnt += 1
            else:
                c_cnt += 1
        
        # 조건: 모음 최소 1개 AND 자음 최소 2개
        if v_cnt >= 1 and c_cnt >= 2:
            # 리스트 안의 문자들을 하나로 합쳐서 출력
            print("".join(path))
        return

    # [유도 파트] 현재 인덱스(idx)부터 마지막 문자(C)까지 순회
    for i in range(idx, C):
        # 1. 일단 현재 문자를 바구니에 넣는다
        path.append(chars[i])
        # 2. 다음 문자를 고르러 떠난다 (중복 방지를 위해 내 다음 칸인 i+1을 넘겨줌)
        dfs(i + 1, path)
        # 3. 중요!! 다음 경우의 수를 위해 방금 넣었던 문자를 다시 뺀다 (백트래킹)
        path.pop()

# 0번 인덱스 문자부터 탐색 시작, 초기 바구니는 비어있음
dfs(0, [])
