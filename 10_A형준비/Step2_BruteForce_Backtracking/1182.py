"""
📍 [BOJ] 1182 부분수열의 합
> [학습 우선순위: Step 2 - 백트래킹/부분집합]
> N개의 정수 중, 원소들을 더해서 합이 S가 되는 모든 경우의 수를 구하라.

🔍 상세 분석:
- 각 숫자에게 물어보는 겁니다. "너, 우리 팀(부분집합)에 들어올래? 말래?"
- 숫자가 N개라면, 각 숫자마다 선택지 2개(O/X)가 있으므로 총 2^N 가지의 경우의 수가 나옵니다.
- N=20이면 2^20 ≒ 100만 번의 연산으로 충분합니다.

🏗️ 구현 체크리스트:
1. DFS 함수 인자로 `idx`(현재 결정할 숫자의 인덱스)와 `current_sum`(지금까지의 합)을 넘긴다.
2. 각 단계에서 두 가지 재귀 호출을 한다:
   - `dfs(idx + 1, current_sum + nums[idx])` : 현재 숫자를 포함하는 경우
   - `dfs(idx + 1, current_sum)` : 현재 숫자를 포함하지 않는 경우
3. 마지막 숫자까지 다 물어봤을 때(`idx == N`), 합이 S와 같은지 확인한다.

💡 학생 가이드:
- "공집합(아무것도 선택 안 함) 주의!" 
  -> 만약 목표 합 S가 0이라면, 아무것도 안 뽑은 경우도 합이 0이라 카운트됩니다. 문제에서는 '크기가 1 이상'이어야 한다고 했으니, S=0일 때는 결과에서 1을 빼줘야 합니다.
"""

import sys
input = sys.stdin.readline

# N: 숫자의 개수, S: 목표 합
N, S = map(int, input().split())
nums = list(map(int, input().split()))

count = 0 # 합이 S가 되는 경우의 수

def dfs(idx, current_sum):
    global count
    
    # [기저 조건] 모든 숫자에 대해 선택(O/X)을 마쳤을 때
    if idx == N:
        if current_sum == S:
            count += 1
        return
        
    # [유도 파트] 현재 숫자(nums[idx])를...
    # 1. 부분수열에 포함시킨다! (합에 더하고 다음 칸으로)
    dfs(idx + 1, current_sum + nums[idx])
    # 2. 부분수열에 안 넣는다! (합은 그대로 두고 다음 칸으로)
    dfs(idx + 1, current_sum)

# 0번 숫자부터 결정 시작, 초기 합은 0
dfs(0, 0)

# 만약 S가 0이라면, 위 로직에서 아무것도 안 뽑은 공집합 경우(합 0)도 1개 카운트됨.
# 문제에서 부분수열의 크기는 1 이상이라 했으므로 1을 빼줌.
if S == 0:
    count -= 1
    
print(count)
