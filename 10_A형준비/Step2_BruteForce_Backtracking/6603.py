"""
📍 [BOJ] 6603 로또
> [학습 우선순위: Step 2 - 백트래킹/조합]
> 주어진 k개의 숫자 중 6개를 고르는 모든 방법을 구하라.

🔍 상세 분석:
- 로또는 '순서가 상관없는' 조합 문제입니다. (1, 2)와 (2, 1)은 같은 것으로 칩니다.
- 따라서 숫자를 고를 때 '이전에 골랐던 것보다 뒤에 있는 숫자'만 고르도록 제한하면 자동으로 중복이 제거되고 정렬된 결과가 나옵니다.

🏗️ 구현 체크리스트:
1. 입력이 0일 때까지 반복한다.
2. DFS 함수 인자로 `idx`(현재 검사할 시작 위치)와 `path`(고른 숫자들)를 넘긴다.
3. 루프를 돌 때 `range(idx, len(S))`를 사용하여 항상 현재 고른 숫자보다 뒤에 있는 것들만 고르게 한다.
4. 6개를 다 골랐다면 출력한다.

💡 학생 가이드:
- "순열과 조합의 차이가 뭔가요?" 
  -> 순열은 `visited`를 써서 처음부터 다시 훑지만, 조합은 `idx`를 써서 항상 앞만 보고 전진합니다. 그래서 조합은 `visited`가 굳이 필요 없습니다!
"""

import sys
input = sys.stdin.readline

def dfs(S, idx, path):
    # [기저 조건] 숫자 6개를 모두 골랐을 때
    if len(path) == 6:
        # 현재까지 고른 로또 번호 출력
        print(*path)
        return
        
    # [유도 파트] 현재 인덱스(idx)부터 리스트 끝까지 숫자를 하나씩 골라본다
    for i in range(idx, len(S)):
        # 1. 숫자 S[i]를 바구니에 넣는다
        path.append(S[i])
        # 2. 다음 숫자를 고르러 떠난다 (중복 방지를 위해 내 뒤에 있는 i+1부터 보게 함)
        dfs(S, i + 1, path)
        # 3. 탐색 끝났으니 다시 뺀다 (백트래킹)
        path.pop()

def solve():
    while True:
        line = list(map(int, input().split()))
        if line[0] == 0: # 마지막 입력이 0이면 프로그램 종료
            break
            
        k = line[0] # 숫자의 개수
        S = line[1:] # 실제 숫자 리스트
        
        # 0번 인덱스부터 탐색 시작
        dfs(S, 0, [])
        # 테스트 케이스 사이에는 빈 줄 하나 출력
        print()

if __name__ == "__main__":
    solve()
