"""
📍 [BOJ] 2231 분해합
> [학습 우선순위: Step 2 - 완전탐색/기초]
> 어떤 자연수 M의 분해합(M + M의 각 자리수 합)이 N인 경우, M을 N의 생성자라고 한다. 가장 작은 생성자를 구하라.

🔍 상세 분석:
- N이 최대 100만입니다. 생성자 M은 무조건 N보다 작습니다.
- 1부터 N까지 모든 숫자를 하나씩 검사(브루트 포스)해도 100만 번의 연산이면 충분합니다 (컴퓨터는 1초에 1억 번 연산 가능!).

🏗️ 구현 체크리스트:
1. 1부터 N까지 반복문을 돌린다.
2. 각 숫자 `i`에 대해 분해합을 구한다.
   - 분해합 = `i` + (i의 각 자리수 합)
3. 만약 분해합이 N과 같다면, `i`가 바로 우리가 찾는 가장 작은 생성자이다.
4. 찾자마자 출력하고 프로그램을 끝낸다 (`return`).
5. 루프가 다 끝날 때까지 못 찾았다면 0을 출력한다.

💡 학생 가이드:
- "가장 작은 생성자를 어떻게 보장하나요?" 
  -> 1부터 커지는 순서대로 검사하기 때문에, 처음으로 조건을 만족하는 `i`가 무조건 가장 작은 생성자가 됩니다.
"""

import sys
input = sys.stdin.readline

def solve():
    # 1. 목표 숫자 N을 입력받는다
    N = int(input())
    
    # 2. 1부터 N까지 모든 후보 숫자를 하나씩 확인한다
    for i in range(1, N + 1):
        # i의 분해합을 계산한다
        # 예: i가 245라면, total = 245 + 2 + 4 + 5 = 256
        total = i + sum(map(int, str(i)))
        
        # 3. 계산된 분해합이 목표 숫자 N과 일치하는지 확인한다
        if total == N:
            # 일치하면 그 숫자가 바로 가장 작은 생성자!
            print(i)
            return
            
    # 4. 루프가 끝날 때까지 일치하는 숫자를 못 찾았다면 0 출력
    print(0)

if __name__ == "__main__":
    solve()
