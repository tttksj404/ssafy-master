"""
📍 [BOJ] 7568 덩치
> [학습 우선순위: Step 2 - 완전탐색/브루트포스]
> 몸무게와 키가 모두 큰 사람을 덩치가 더 크다고 한다. 자신보다 덩치가 큰 사람의 수 + 1이 자신의 등수다.

🔍 상세 분석:
- 모든 사람을 한 명씩 지목해서, 나머지 모든 사람들과 일일이 비교하면 됩니다.
- N이 50명이므로, 50 * 50 = 2,500번만 비교하면 끝나는 아주 쉬운 완전탐색 문제입니다.

🏗️ 구현 체크리스트:
1. N명의 몸무게와 키 정보를 리스트에 저장한다.
2. 한 명(i)을 기준으로 잡는다.
3. 나머지 사람(j)들과 비교하며, (j)가 (i)보다 몸무게와 키가 둘 다 큰지 확인한다.
4. 더 큰 사람이 발견될 때마다 등수(`rank`)를 1씩 올린다.
5. 모든 사람에 대해 등수를 구하고 출력한다.

💡 학생 가이드:
- "등수를 왜 1부터 시작하나요?" 
  -> 문제에서 등수는 '나보다 큰 사람의 수 + 1'이라고 정의했기 때문입니다. 나보다 큰 사람이 0명이면 1등이 되는 원리죠!
"""

import sys
input = sys.stdin.readline

def solve():
    # 1. 사람의 수 N 입력
    N = int(input())
    # 2. 각 사람의 (몸무게, 키) 정보를 리스트에 담는다
    people = []
    for _ in range(N):
        people.append(list(map(int, input().split())))
        
    # 3. 각 사람별로 등수를 매긴다
    ans = []
    for i in range(N):
        # 기본 등수는 1등에서 시작
        rank = 1
        for j in range(N):
            # i번째 사람보다 j번째 사람의 덩치가 '확실히' 큰 경우만 카운트
            # 둘 중 하나만 크면 덩치가 큰 것이 아님!
            if people[i][0] < people[j][0] and people[i][1] < people[j][1]:
                rank += 1
        ans.append(rank)
        
    # 4. 모든 등수를 공백으로 구분하여 출력
    print(*ans)

if __name__ == "__main__":
    solve()
