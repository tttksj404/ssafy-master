"""
📍 [BOJ] 11286 절댓값 힙
> [학습 우선순위: Step 1 - 기초/자료구조]
> 1. 절댓값이 가장 작은 값을 먼저 뽑는다.
> 2. 절댓값이 같다면, 실제 숫자가 더 작은 것을 먼저 뽑는다.

🔍 상세 분석:
- 단순히 숫자 하나가 아니라, '절댓값'과 '실제값'이라는 두 가지 기준이 필요합니다.
- 파이썬의 힙(`heapq`)에 '튜플(tuple)'을 넣으면 아주 재미있는 일이 일어납니다.
- `(기준1, 기준2)`를 넣으면 기준1을 먼저 비교하고, 기준1이 같으면 기준2를 비교해서 정렬해줍니다.
- 즉, `(절댓값, 실제값)` 순서로 힙에 넣으면 문제가 요구하는 조건이 자동으로 해결됩니다!

🏗️ 구현 체크리스트:
1. 입력 x가 0인지 아닌지 판단한다.
2. x가 0이 아니면: `heapq.heappush(heap, (abs(x), x))` 를 수행한다.
3. x가 0이면: 힙에서 튜플을 꺼내고(`heappop`), 그 중 실제값인 `[1]`번 인덱스 요소를 출력한다.

💡 학생 가이드:
- "튜플 정렬의 힘!" 
  -> `(1, -5)`와 `(1, 5)`가 힙에 있다면? 첫 번째 요소(절댓값 1)가 같으므로 두 번째 요소(-5와 5)를 비교합니다. -5가 더 작으므로 `(1, -5)`가 먼저 나오게 됩니다. 문제 조건과 딱 맞죠?
"""

import heapq
import sys
input = sys.stdin.readline

def solve():
    N = int(input())
    abs_heap = [] # (절댓값, 실제값) 튜플을 담을 힙
    
    for _ in range(N):
        x = int(input())
        
        if x == 0:
            # 출력 요청
            if not abs_heap:
                # 비어있으면 0 출력
                print(0)
            else:
                # 힙에서 최우선 순위 튜플을 꺼냄
                # 튜플은 (절댓값, 실제값) 형태이므로 [1]이 실제 숫자임
                print(heapq.heappop(abs_heap)[1])
        else:
            # 삽입 요청: 절댓값을 최우선 기준으로, 실제값을 차선 기준으로 튜플 생성
            heapq.heappush(abs_heap, (abs(x), x))

if __name__ == "__main__":
    solve()
