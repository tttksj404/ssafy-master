"""
📍 [BOJ] 2577 숫자의 개수
> [학습 우선순위: Step 1 - 기초/문자열]
> 세 개의 자연수 A, B, C를 곱한 결과에 0부터 9까지 각각의 숫자가 몇 번씩 쓰였는지 구하는 프로그램.

🔍 상세 분석:
- 곱셈 결과는 매우 큰 숫자일 수 있습니다. 이를 각 자리별로 확인하려면 '문자열'로 변환하는 것이 가장 쉽습니다.
- 숫자의 빈도를 저장하기 위해 0~9번 인덱스를 가진 리스트(크기 10)를 준비합니다.

🏗️ 구현 체크리스트:
1. A, B, C 세 수를 곱한 결과값을 구한다.
2. 결과값을 `str()` 함수로 문자열로 바꾼다. (그래야 한 글자씩 떼어볼 수 있음)
3. 0~9까지 숫자를 카운트할 `[0, 0, ..., 0]` 리스트를 만든다.
4. 문자열을 한 글자씩 돌며 해당 숫자의 인덱스 값을 1씩 증가시킨다.

💡 학생 가이드:
- "int(char)가 왜 필요한가요?" 
  -> 문자열 '7'은 숫자가 아니라 글자입니다. 리스트의 인덱스로 쓰려면 실제 숫자인 7로 바꿔줘야 하기 때문입니다.
"""

import sys
input = sys.stdin.readline

def solve():
    # 1. 세 수를 입력받아 곱한다
    A = int(input())
    B = int(input())
    C = int(input())
    
    # 2. 곱한 결과를 문자열로 바꾼다 (예: 17037300 -> "17037300")
    res_str = str(A * B * C)
    
    # 3. 0부터 9까지 빈도를 셀 바구니(리스트)를 만든다
    # counts[0]은 '0'의 개수, counts[9]는 '9'의 개수를 담게 됨
    counts = [0] * 10
    
    # 4. 문자열을 한 글자씩 꺼내어 확인한다
    for char in res_str:
        # 글자를 숫자로 바꿔서(int), 해당 인덱스의 바구니 숫자를 1 올린다
        digit = int(char)
        counts[digit] += 1
        
    # 5. 마지막으로 0번부터 9번까지 담긴 숫자를 한 줄씩 출력한다
    for c in counts:
        print(c)

if __name__ == "__main__":
    solve()
