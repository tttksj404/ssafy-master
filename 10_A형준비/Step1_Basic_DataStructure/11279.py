"""
📍 [BOJ] 11279 최대 힙
> [학습 우선순위: Step 1 - 기초/자료구조]
> 힙(heap)을 이용해 가장 큰 값을 빠르게 뽑아내고 제거하는 프로그램.

🔍 상세 분석:
- '가장 큰 값'을 실시간으로 계속 뽑아야 합니다. 리스트를 매번 정렬하면 너무 느립니다.
- '힙'이라는 자료구조는 가장 큰(혹은 작은) 값을 뽑는 데 특화되어 있습니다 (O(log N)).
- 주의: 파이썬의 `heapq` 모듈은 '최소 힙(가장 작은 게 맨 위)'으로 동작합니다. 
- 꼼수: 숫자에 `-`를 붙여서 넣으면, 가장 큰 숫자가 가장 작은 숫자가 됩니다! (예: 10, 5 -> -10, -5 -> -10이 더 작음)

🏗️ 구현 체크리스트:
1. `heapq` 모듈을 임포트한다.
2. 입력 x가 0이면 힙에서 값을 꺼내고, x가 자연수면 힙에 넣는다.
3. 넣을 때: `heapq.heappush(heap, -x)`
4. 뺄 때: `-heapq.heappop(heap)`

💡 학생 가이드:
- "왜 -를 붙여서 넣나요?" 
  -> 파이썬 힙은 최솟값을 뽑아줍니다. 100과 1이 있을 때 -100과 -1로 바꾸면, -100이 최솟값이 되어 먼저 나오게 됩니다. 이걸 다시 -를 붙여 출력하면 100(최댓값)이 나오는 원리입니다!
"""

import heapq
import sys

# 입력이 많으므로 readline 사용 필수
input = sys.stdin.readline

def solve():
    # 연산의 개수 N
    n = int(input())
    max_heap = []

    for _ in range(n):
        x = int(input())
        
        # 1. x가 0이면 '최댓값 출력 및 제거' 요청
        if x == 0:
            if not max_heap:
                # 힙이 비어있으면 0 출력
                print(0)
            else:
                # 힙에서 꺼낸 값(음수)에 다시 -를 붙여서 원래의 양수 최댓값으로 복구
                print(-heapq.heappop(max_heap))
        
        # 2. x가 자연수면 '힙에 삽입' 요청
        else:
            # 파이썬 최소 힙을 최대 힙처럼 쓰기 위해 부호를 반대로 넣어줌
            heapq.heappush(max_heap, -x)

if __name__ == "__main__":
    solve()
