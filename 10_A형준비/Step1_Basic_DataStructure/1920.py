"""
📍 [BOJ] 1920 수 찾기
> [학습 우선순위: Step 1 - 기초/탐색]
> N개의 정수들 속에 특정 숫자 X가 들어있는지 확인하는 프로그램.

🔍 상세 분석:
- N과 M이 최대 10만입니다. 만약 리스트에서 하나씩 찾으면(O(N)) 최악의 경우 10만 * 10만 = 100억 번의 연산이 필요해 시간 초과가 납니다.
- 해결책 1: 이진 탐색 (O(log N)) - 정렬된 상태에서 반씩 쪼개며 찾기.
- 해결책 2: 해시 셋 (O(1)) - 파이썬의 `set` 자료형을 사용하면 찾기가 순식간에 끝납니다. (이게 훨씬 빠르고 쉽습니다!)

🏗️ 구현 체크리스트:
1. 기준이 되는 숫자들을 입력받아 `set()`으로 저장한다. (중복 제거 및 탐색 효율 극대화)
2. 찾고 싶은 숫자들을 하나씩 순회한다.
3. `in` 연산자를 써서 존재 여부를 확인한다. (set에서의 `in`은 거의 즉시 결과가 나옴)

💡 학생 가이드:
- "리스트(`[]`)와 세트(`{}`)의 `in`은 무엇이 다른가요?" 
  -> 리스트는 첫 칸부터 끝까지 다 뒤져봐야 하지만, 세트는 '해시 테이블'이라는 지도를 보고 바로 찾아가기 때문에 데이터가 많을수록 세트가 압도적으로 빠릅니다.
"""

import sys
input = sys.stdin.readline

def solve():
    # N: 기준 숫자들의 개수
    N = int(input())
    # 1. 기준 숫자들을 세트(set)에 담는다. (찾기 성능 O(1) 보장)
    A = set(map(int, input().split()))
    
    # M: 확인할 숫자들의 개수
    M = int(input())
    # 확인할 숫자들을 리스트로 받는다
    queries = list(map(int, input().split()))
    
    # 2. 확인할 숫자들을 하나씩 꺼내어 세트에 있는지 물어본다
    for q in queries:
        if q in A:
            # 있으면 1 출력
            print(1)
        else:
            # 없으면 0 출력
            print(0)

if __name__ == "__main__":
    solve()
